// Event lookups used by chart.js and ledger.js (fall back to English data).
function evTitle(ev){ const i = EVENTS.indexOf(ev); return isBn() && EVENTS_BN[i] ? EVENTS_BN[i].title : ev.title; }
function evBody(ev){ const i = EVENTS.indexOf(ev); return isBn() && EVENTS_BN[i] ? EVENTS_BN[i].body : ev.body; }

/* ---------- Engine ---------- */
function applyStatic(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    if(el._en === undefined) el._en = el.innerHTML;       // capture English once
    const k = el.dataset.i18n;
    el.innerHTML = isBn() ? (BN[k] !== undefined ? BN[k] : el._en) : el._en;
  });
}
function applyLang(lang){
  I18N.lang = (lang === 'bn') ? 'bn' : 'en';
  document.documentElement.lang = I18N.lang;
  document.documentElement.setAttribute('data-lang', I18N.lang);
  try{ localStorage.setItem('lot-lang', I18N.lang); }catch(e){}
  const btn = document.getElementById('langToggle');
  if(btn){
    btn.setAttribute('aria-pressed', String(isBn()));
    const cur = btn.querySelector('.lt-cur');
    if(cur) cur.textContent = isBn() ? 'English' : 'বাংলা';
  }
  applyStatic();
  applyPartials();
  I18N.rerender.forEach(fn=>{ try{ fn(); }catch(e){ console.error('i18n rerender', e); } });
  // Language-toggle contract: every peoples-edition component (danger map,
  // widgets) re-renders on this event and reads document.documentElement.dataset.lang.
  document.dispatchEvent(new Event('langchange'));
}
// Peoples-edition partials carry their own data-bn / data-en text (no dictionary
// key). Swap textContent in place so the new sections follow the same toggle.
function applyPartials(){
  document.querySelectorAll('[data-bn]').forEach(el=>{
    const t = isBn() ? el.dataset.bn : (el.dataset.en !== undefined ? el.dataset.en : el.dataset.bn);
    if(t !== undefined) el.textContent = t;
  });
}
function initI18N(){
  // Bangla is the default edition; English is opt-in via the toggle or a stored choice.
  const saved = (localStorage.getItem('lot-lang') === 'en') ? 'en' : 'bn';
  const btn = document.getElementById('langToggle');
  if(btn) btn.addEventListener('click', ()=> applyLang(isBn() ? 'en' : 'bn'));
  applyLang(saved);
}
// Scripts sit at the end of <body>, so DOMContentLoaded fires after every
// view has registered its re-render hook, safe to run the first apply here.
document.addEventListener('DOMContentLoaded', initI18N);
